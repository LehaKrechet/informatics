// Входные данные
// Даны три целых числа, каждое записано в отдельной строке.

// Выходные данные
// Выведите наибольшее из данных чисел (программа должна вывести ровно одно целое число).

#include <iostream>
#include <iomanip>
int main()
{
	int number_a, number_b, number_c, result;
	std::cin >> number_a;
	std::cin >> number_b;
	std::cin >> number_c;
	if (number_a >= number_b and number_a >= number_c) {
		result = number_a;
	}else if (number_b >= number_a and number_b >= number_c) {
		result = number_b;
	}else if (number_c >= number_a and number_c >= number_b) {
		result = number_c;
	}
	std::cout << result;
}


